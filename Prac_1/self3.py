# arithimetic operators

# + operator
print(f"10 + 20 = {10 + 20}")
# 10 + 20 = 30


# - operator
print(f"10 - 20 = {10 - 20}")
# 10 - 20 = -10

# / operator
# true division operator
# the result will be always in float
print(f"10 / 3 = {10 / 3}")
# 10 / 3 = 3.3333333333333335

# // operator
# floor division operator
# the result will be always in int
print(f"10 // 3 = {10 // 3}")
# 10 // 3 = 3


# * operator [used for mathematical multiplication operation]
# with both numeric values
print(f"10 * 3 = {10 * 3}")
# 10 * 3 = 30


# * operator [string repeating operation]
print(f"test * 3 = {'test' * 3}")
# test * 3 = testtesttest

print('-*-' * 10)
# -*--*--*--*--*--*--*--*--*--*-


# ** operator
# used to get the power value
print(f"square of 5 = {5 ** 2}")
print(f"cube of 3 = {3 ** 3}")
print(f"4th power of 15 = {15 ** 4}")

# square of 5 = 25
# cube of 3 = 27
# 4th power of 15 = 50625