# Relational operators

n1 = 10
n2 = 20

print(f"{n1} > {n2} = {n1 > n2}")
# 10 > 20 = False

print(f"{n1} < {n2} = {n1 < n2}")
# 10 < 20 = True

print(f"{n1} >= {n2} = {n1 >= n2}")
# 10 >= 20 = False

print(f"{n1} <= {n2} = {n1 <= n2}")
# 10 <= 20 = True

print(f"{n1} == {n2} = {n1 == n2}")
# 10 == 20 = False

print(f"{n1} != {n2} = {n1 != n2}")
# 10 != 20 = True
